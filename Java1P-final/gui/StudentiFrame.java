    private DefaultListModel<Student> model;

    public StudentiFrame() {
        initComponents();
        
        model = new DefaultListModel<>();
        jList1.setModel(model);
        
        jButton1.addActionListener(ev -> addStudent());
        
        //Stergere produse la dublu-click
        jList1.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(MouseEvent ev){
                if(ev.getClickCount() == 2){
                    Student student = jList1.getSelectedValue();
                    MainService.getInstance().deleteStudent(student.getId());
                    showStudents();
                }
            }
        });
        
        showStudents();
        
        setLocationRelativeTo(null);
    }
    
    private void showStudents(){
        List<Student> students = MainService.getInstance().getAllStudents();
        model.clear();
        
        //pentru fiecare produs, adauga in model produsul din lista
        students.forEach(model::addElement);
    }
    
    private void addStudent(){
        String nume = jTextField1.getText();
        String prenume = jTextField2.getText();
        String cnp = jTextField3.getText();
        
        Student s = new Student(nume, prenume, cnp);
        
        if(MainService.getInstance().addStudent(s)){
            showStudents();
            JOptionPane.showMessageDialog(null, "Student added successfully!");
        }else{
            JOptionPane.showMessageDialog(null, "Error: CNP already exists!");
        }
    }
    
